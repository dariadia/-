DELIMITER //
DROP PROCEDURE IF EXISTS hello;
CREATE PROCEDURE hello ()
BEGIN
	DECLARE now TIME DEFAULT CURRENT_TIME();
    IF now BETWEEN '06:00:00' AND '12:00:00' THEN
		SELECT 'Доброе утро!' AS greeting;
	ELSEIF now BETWEEN '12:00:00' AND '18:00:00' THEN
		SELECT 'Добрый день!' AS greeting;
	ELSEIF now BETWEEN '18:00:00' AND '00:00:00' THEN
		SELECT 'Добрый вечер!' AS greeting;
	ELSE
		SELECT 'Доброй ночи!' AS greeting;
	END IF;
END//

