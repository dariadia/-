CREATE TRIGGER not_null_i BEFORE INSERT ON products
FOR EACH ROW
BEGIN
  DECLARE new_name TEXT;
  DECLARE new_description TEXT;
  
  IF new_name IS NOT NULL OR new_description IS NOT NULL THEN
		SET NEW.name = new_name;
        SET NEW.description = new_description;
	ELSE 
